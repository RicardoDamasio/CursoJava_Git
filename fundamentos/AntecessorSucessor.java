package fundamentos;

public class AntecessorSucessor {
	public static void main(String[] args) {
		int n = 0;
		int antecessor = n-1;
		int sucessor = n+1;
		
		System.out.println("O número escolhido é "+ n);
		System.out.println("Seu Antecessor é "+ antecessor);
		System.out.println("Seu Sucessor é "+ sucessor);
		
	}

}
