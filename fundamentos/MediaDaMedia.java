package fundamentos;

public class MediaDaMedia {
	
	public static void main(String[] args) {
		double PrimeiraMedia  = (8.0+9.0+7.0)/3;
		double SegundaMedia  = (4.0+5.0+6.0)/3;
		double SomaDasMedias = PrimeiraMedia+SegundaMedia;
		double MediaDasMedidas = SomaDasMedias/2;
		
		
		System.out.println("A Primeira Media é = "+ PrimeiraMedia);
		System.out.println("A Segunda Media é = "+ SegundaMedia);
		System.out.println("A Soma das Médias é = "+ SomaDasMedias);
		System.out.println("A Média das Medias é = "+ MediaDasMedidas);
	}

}
